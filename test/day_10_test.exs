    input = """
    .#..#
    .....
    #####
    ....#
    ...##
    """

    assert {{3, 4}, 8} == part1(input)
  end

  test "part1 b" do
    input = """
        ......#.#.
        #..#.#....
        ..#######.
        .#.#.###..
        .#..#.....
        ..#....#.#
        #..#....#.
        .##.#..###
        ##...#..#.
        .#....####
    """

    # I think the example is wrong here.
    # the example says {{5,8}, 33}
